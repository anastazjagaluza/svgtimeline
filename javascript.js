//January
document.querySelector("#Jan").addEventListener("mouseover", () => {
  document.querySelector("#Janline").style.display = "block";
  document.querySelector("#Janline").classList.add("drawline");
  document.querySelector("#Jandetails").style.display = "block";
  document.querySelector("#Jansquare").classList.add("openup");
  document.querySelector("#Jan").style.cursor = "pointer";
  document.querySelector("#Line").style.strokeWidth = "10";
});
document.querySelector("#Jan").addEventListener("mouseout", () => {
  document.querySelector("#Janline").classList.remove("drawline");
  document.querySelector("#Jansquare").classList.remove("openup");
  document.querySelector("#Line").style.strokeWidth = "1";
  document.querySelector("#Jandetails").style.display = "none";
  document.querySelector("#Janline").style.display = "none";
});
